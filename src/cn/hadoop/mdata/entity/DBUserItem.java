package cn.hadoop.mdata.entity;

public class DBUserItem {
	
	private String uid;
	
	private String address;
	
	private String dispalyinfo;

	/**
	 * @return the uid
	 */
	public String getUid() {
		return uid;
	}

	/**
	 * @param uid the uid to set
	 */
	public void setUid(String uid) {
		this.uid = uid;
	}

	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}

	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}

	/**
	 * @return the dispalyinfo
	 */
	public String getDispalyinfo() {
		return dispalyinfo;
	}

	/**
	 * @param dispalyinfo the dispalyinfo to set
	 */
	public void setDispalyinfo(String dispalyinfo) {
		this.dispalyinfo = dispalyinfo;
	}
	
	
}
